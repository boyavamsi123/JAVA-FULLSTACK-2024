public class Logical{
    public static void main(String[] args){
        int a=7, b=11;
        System.out.println("A is" + a + "B is" + b);  //A is7B is11
        System.out.println("Answer for == operator :"+(a==b));  //false
        System.out.println("Answer for != operator :"+(a!=b));  //true
        System.out.println("Answer for > operator :"+(a>b));    //false
        System.out.println("Answer for < operator :"+(a<b));    //true
        System.out.println("Answer for >= operator :"+(a>=b));  //false
        System.out.println("Answer for <= operator :"+(a<=b));  //true
    }
}